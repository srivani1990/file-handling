#include<stdio.h>
int main()
{
float i=20.16, j=6.12;
printf("The Addition of %f and %f is: %f\n",i,j,i+j);
printf("The Subtraction of %f and %f is: %f\n",i,j,i-j);
printf("The Multiplication of %f and %f is: %f\n",i,j,i*j);
printf("The Division of %f and %f is: %f\n",i,j,i/j);
printf("The Incremented value ++i is: %f\n",++i);
printf("The Decremented value --j is: %f\n",--j);
int a=30,b=25;
printf("The Modulus operation of %d and %d is: %d\n",a,b,a%b);
return 0;
}

